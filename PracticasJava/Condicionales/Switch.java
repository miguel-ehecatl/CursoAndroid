public class Switch{
	
	public static void main(String[] args) {
	
		int edad = 39;

		switch(edad){
			case 18:
			System.out.println("Tienes 18");
			break;
			case 19:
			System.out.println("Tienes 19");
			break;
			case 20:
			System.out.println("Tienes 20");
			break;
			default:
			System.out.println("No tienes 18,19 o 20");
			break;
		}

	}

}